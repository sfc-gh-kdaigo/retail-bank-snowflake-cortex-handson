USE ROLE ACCOUNTADMIN;

-- データベースの作成
CREATE DATABASE IF NOT EXISTS RETAIL_BANKING_DB;

USE DATABASE RETAIL_BANKING_DB;

-- ウェアハウスの作成
CREATE WAREHOUSE IF NOT EXISTS RETAIL_BANKING_WH
    WAREHOUSE_SIZE = 'SMALL'
    AUTO_SUSPEND = 60
    AUTO_RESUME = TRUE;

USE WAREHOUSE RETAIL_BANKING_WH;

-- ---------------------------------------------------------
-- Step 1-2: クロスリージョン推論の有効化
-- ---------------------------------------------------------
-- Cortex AI機能を使用するために必要な設定
-- 日本リージョン（AP_NORTHEAST_1）では一部のLLMモデルが利用できないため、
-- クロスリージョン推論を有効にすることで、他リージョンのモデルを利用可能にします
ALTER ACCOUNT SET CORTEX_ENABLED_CROSS_REGION = 'ANY_REGION';

-- ---------------------------------------------------------
-- Step 1-3: スキーマ作成
-- ---------------------------------------------------------
-- 日本語カラム名版スキーマ
CREATE SCHEMA IF NOT EXISTS RETAIL_BANKING_DB.RETAIL_BANKING_JP;

-- 英語カラム名版スキーマ
CREATE SCHEMA IF NOT EXISTS RETAIL_BANKING_DB.RETAIL_BANKING_EN;

-- 分析用スキーマ（Semantic View格納用）
CREATE SCHEMA IF NOT EXISTS RETAIL_BANKING_DB.ANALYTICS;

-- エージェント用スキーマ（Cortex Agent格納用）
CREATE SCHEMA IF NOT EXISTS RETAIL_BANKING_DB.AGENT;

-- 非構造化データ用スキーマ（Cortex Search用）
CREATE SCHEMA IF NOT EXISTS RETAIL_BANKING_DB.UNSTRUCTURED_DATA;

-- Git統合用スキーマ（API Integration, Git Repository格納用）
CREATE SCHEMA IF NOT EXISTS RETAIL_BANKING_DB.GIT;

-- ---------------------------------------------------------
-- Step 1-4: ドキュメント格納用ステージの作成
-- ---------------------------------------------------------
-- PDFファイルをアップロードするための内部ステージを作成
USE SCHEMA RETAIL_BANKING_DB.UNSTRUCTURED_DATA;

CREATE OR REPLACE STAGE document_stage
    DIRECTORY = (ENABLE = TRUE)
    ENCRYPTION = (TYPE = 'SNOWFLAKE_SSE');

-- ステージ確認
-- LIST @document_stage;

-- ---------------------------------------------------------
-- Step 1-5: Git連携 - API統合の作成
-- ---------------------------------------------------------
-- GitHubリポジトリと連携するためのAPI統合を作成
-- これにより、GitHubからPDFファイルを直接Snowflakeに取り込むことができます
USE SCHEMA RETAIL_BANKING_DB.GIT;

CREATE OR REPLACE API INTEGRATION git_api_integration
    API_PROVIDER = git_https_api
    API_ALLOWED_PREFIXES = ('https://github.com/sfc-gh-kdaigo/')
    ENABLED = TRUE;

-- ---------------------------------------------------------
-- Step 1-6: Git連携 - Gitリポジトリの作成
-- ---------------------------------------------------------
-- ハンズオン用のGitHubリポジトリを登録
-- リポジトリURL: https://github.com/sfc-gh-kdaigo/retail-bank-snowflake-cortex-handson

CREATE OR REPLACE GIT REPOSITORY retail_bank_handson
    API_INTEGRATION = git_api_integration
    ORIGIN = 'https://github.com/sfc-gh-kdaigo/retail-bank-snowflake-cortex-handson.git';

-- Gitリポジトリの確認
SHOW GIT REPOSITORIES;

-- ---------------------------------------------------------
-- Step 1-7: GitからステージへPDFファイルをコピー
-- ---------------------------------------------------------
-- GitHubリポジトリからPDFファイルを内部ステージにコピー

-- 商品説明書・マニュアル等をコピー
COPY FILES 
    INTO @RETAIL_BANKING_DB.UNSTRUCTURED_DATA.document_stage/
    FROM @retail_bank_handson/branches/main/files/
    PATTERN = '.*\.pdf';

-- ディレクトリメタデータを更新
ALTER STAGE RETAIL_BANKING_DB.UNSTRUCTURED_DATA.document_stage REFRESH;

-- ステージ内のファイル確認
LIST @RETAIL_BANKING_DB.UNSTRUCTURED_DATA.document_stage;


