USE SCHEMA RETAIL_BANKING_DB.RETAIL_BANKING_JP;

-- ---------------------------------------------------------
-- 顧客数の確認
-- ---------------------------------------------------------
SELECT 
    CASE WHEN "人格コード" = 1 THEN '個人' 
         WHEN "人格コード" = 2 THEN '法人' 
         WHEN "人格コード" = 3 THEN '個人事業主'
         ELSE 'その他' END AS "顧客区分",
    COUNT(*) AS "顧客数"
FROM "顧客基本属性情報＿月次"
WHERE "元帳状態表示" = 0  -- 活動中のみ
GROUP BY "人格コード"
ORDER BY "顧客数" DESC;

-- ---------------------------------------------------------
-- 名寄せ状況の確認
-- ---------------------------------------------------------
-- 同一人物・同一法人が複数の顧客番号を持つケースを抽出
-- 例: 個人口座と個人事業主口座、本店と支店、旧姓と新姓 など
SELECT 
    i."統合顧客番号",
    COUNT(*) AS "紐づき顧客数",
    LISTAGG(c."漢字氏名１", ', ') AS "顧客名一覧"
FROM "統合顧客＿インデクス＿月次" i
JOIN "顧客基本属性情報＿月次" c 
    ON i."金融機関コード" = c."金融機関コード" 
    AND i."顧客番号" = c."顧客番号"
    AND i."基準日" = c."基準日"
GROUP BY i."統合顧客番号"
HAVING COUNT(*) > 1  -- 2件以上 = 名寄せ対象
ORDER BY "紐づき顧客数" DESC;

-- ---------------------------------------------------------
-- 取引金額の集計
-- ---------------------------------------------------------
SELECT 
    CASE WHEN "入払区分" = 1 THEN '入金' ELSE '出金' END AS "入出金区分",
    COUNT(*) AS "取引件数",
    SUM("取引金額") AS "総取引金額"
FROM "流動性預金取引データ＿日次"
WHERE "取消取引表示" = 0  -- 取消以外
GROUP BY "入払区分";

-- ---------------------------------------------------------
-- チャネル別取引件数
-- ---------------------------------------------------------
SELECT 
    CASE 
        WHEN "チャネル識別コード" = 1 THEN '窓口'
        WHEN "チャネル識別コード" = 2 THEN 'ATM'
        WHEN "チャネル識別コード" = 3 THEN 'オンライン'
        ELSE 'その他'
    END AS "チャネル",
    COUNT(*) AS "取引件数",
    SUM("取引金額") AS "総取引金額"
FROM "流動性預金取引データ＿日次"
WHERE "取消取引表示" = 0
GROUP BY "チャネル識別コード"
ORDER BY "取引件数" DESC;


